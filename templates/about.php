<h2>What is MyMentor.Codes</h2>
<hr>
<p>MyMentor.Codes (MMC) is an online mentorship program for programmers. We are more than just another programming forum website, 
	we are a community dedicated in helping one another reach our goals. This is done through the use of mentors. Anyone and everyone can be a mentor. 
	There is always someone more experienced than you, and there is always someone who can learn from you. MMC helps to find these people for you so you're 
	able to learn from those who've been in your shoes, while mentoring another individual who aspires to be where you're at.</p>
	
	<br>
<h3>How can you help?</h3>
<hr>
<p>MMC is an open source project on GitHub. If you want to help in creating this project, please go 
	to <a href="http://github.com/Amallard/MyMentor.Codes" target="_blank">our GitHub page</a> to contribute. </p>
	
